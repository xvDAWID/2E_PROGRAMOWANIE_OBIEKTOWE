#include <iostream>

using namespace std;

double obwodPr(double bok1, double bok2) {
    double obwodPr;
    obwodPr = 2 * bok1 + 2 * bok2;

    return obwodPr;
}

int main()
{
    double bok1, bok2;
    cout << "Podaj pierwszy bok prostokata" << endl; cin >> bok1;
    cout << "Podaj drugi bok prostokata" << endl; cin >> bok2;
    cout << "Oto obwod prostokata: " << obwodPr(bok1, bok2);

}
